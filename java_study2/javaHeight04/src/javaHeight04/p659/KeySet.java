package javaHeight04.p659;

public class KeySet {

	private String teacher;
	private Data2 data2;
	
	public KeySet(String teacher, Data2 data2) {
		super();
		this.teacher = teacher;
		this.data2 = data2;
	}
	public String getTeacher() {
		return teacher;
	}
	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}
	public Data2 getData2() {
		return data2;
	}
	public void setData2(Data2 data2) {
		this.data2 = data2;
	}
	
	
}
