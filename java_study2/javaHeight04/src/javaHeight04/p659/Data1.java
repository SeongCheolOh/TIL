package javaHeight04.p659;

public class Data1 {
	private int point;
	private String address;
	private Shop shop;
	
	
	public Data1(int point, String address) {
		super();
		this.point = point;
		this.address = address;
	}
	
	
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public Shop getShop() {
		return shop;
	}
	public void setShop(Shop shop) {
		this.shop = shop;
	}
	
	
}
