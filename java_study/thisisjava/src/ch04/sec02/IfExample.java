package ch04.sec02;

public class IfExample {

	public static void main(String[] args) {
		// if문
		int score = 93;
		
		if(score>= 90) {
			System.out.println("90점 이상입니다");
			System.out.println("등급은 a입니다");
		}
		if(score< 90) {
			System.out.println("90점 미만입니다");
			System.out.println("등급은 b입니다");
		}
		

	}// end main

}// end class
